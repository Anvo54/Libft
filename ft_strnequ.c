#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	if (!s1 || !s2)
		return (0);
	if (n == 0)
		return (1);
	if (s1[0] == 0 && s2[0] == 0)
		return (1);
	if (s1[0] == 0 || s2[0] == 0)
		return (0);
	while (s1[i] && s2[i])
	{
		if ((s1[i + 1] != s2[i + 1] || s1[i] != s2[i]) && n > 1)
			return (0);
		i++;
		n--;
	}
	return (1);
}
